// Full-color custom SVGs (multi-path, each path has its own fill)
const FULL_COLOR_SVGS: Record<string, { svg: React.ReactNode; viewBox: string }> = {
  figma: { svg: FIGMA_SVG, viewBox: "0 0 600 600" },
  "firebase-studio": { svg: FIREBASE_STUDIO_SVG, viewBox: "0 0 192 192" },
};

type Props = {
  name: string;
  className?: string;
};

export function ToolIcon({ name, className }: Props) {
  const info = TOOL_ICON_MAP[name];

  // Full-color brand logos (Figma, Firebase Studio)
  if (info?.custom && FULL_COLOR_SVGS[info.custom]) {
    const { svg, viewBox } = FULL_COLOR_SVGS[info.custom];
    return (
      <svg
        role="img"
        viewBox={viewBox}
        className={className}
        aria-label={name}
      >
        {svg}
      </svg>
    );
  }

  // Brand logo via simple-icons — uses currentColor (text color)
  if (info?.slug) {
    const icon = (si as Record<string, { path: string; hex: string }>)[info.slug];
    if (icon) {
      return (
        <svg
          role="img"
          viewBox="0 0 24 24"
          className={className}
          fill="currentColor"
          aria-label={name}
        >
          <path d={icon.path} />
        </svg>
      );
    }
  }

  // Custom single-color SVG (Adobe, ChatGPT) — uses currentColor
  if (info?.custom && CUSTOM_SVGS[info.custom]) {
    return (
      <svg
        role="img"
        viewBox="0 0 24 24"
        className={className}
        fill="currentColor"
        aria-label={name}
      >
        <path d={CUSTOM_SVGS[info.custom]} />
      </svg>
    );
  }

  // Lucide fallback
  const Lucide = info?.lucide ?? PenTool;
  return <Lucide className={className} aria-label={name} />;
}
